type RowSeed = [
  entityId: string,
  score: number,
  momentum: number,
  category: string,
  sourceQuality?: SourceQuality,
];

const clamp = (value: number) => Math.max(45, Math.min(99, value));

const makeSparkline = (score: number, momentum: number, rank: number) =>
  Array.from({ length: 10 }, (_, index) =>
    clamp(
      Math.round(score - 8 + index * (momentum / 95) + ((index + rank) % 3)),
    ),
  );

const makeDimensions = (
  score: number,
  momentum: number,
  quality: SourceQuality,
): DimensionScore[] => {
  const qualityOffset = quality === "high" ? 2 : quality === "medium" ? 0 : -3;
  const offsets = [2, 1, 0, 1, -1, 0, Math.round((momentum - 78) / 6), qualityOffset];

  return dimensionTemplates.map((dimension, index) => ({
    ...dimension,
    score: clamp(Math.round(score + offsets[index])),
  }));
};

const signalLabel = (score: number) => {
  if (score >= 92) return "Very high proxy";
  if (score >= 84) return "High proxy";
  if (score >= 76) return "Medium proxy";
  return "Early proxy";
};

const sourceIdsForEntity = (entityId: string, quality: SourceQuality) => {
  const target = entities.find((item) => item.id === entityId);
  const base = target?.sourceIds ?? ["company-official"];
  const qualitySource =
    quality === "high"
      ? "mlcommons-benchmarks"
      : quality === "medium"
        ? "coinmarketcap-reference"
        : "community-signal";

  return Array.from(new Set([...base, qualitySource]));
};

const rowsForTrack = (trackId: string, seeds: RowSeed[]): RankingRow[] =>
  seeds.map(([entityId, score, momentum, category, sourceQuality], index) => {
    const quality = sourceQuality ?? "medium";
    const entityStage =
      entities.find((item) => item.id === entityId)?.stage ?? "Prototype";

    return {
      entityId,
      trackId,
      rank: index + 1,
      score,
      scoreChange: Number(((momentum - 80) / 10).toFixed(1)),
      momentum,
      category,
      trafficProxy: signalLabel(score),
      fundingProxy: entityStage,
      launchDate: "See profile",
      addedDate: checkedDate,
      githubSignal: signalLabel(momentum),
      researchSignal: signalLabel(Math.round((score + momentum) / 2)),
      patentSignal: signalLabel(score - 4),
      sentiment: clamp(Math.round((score + momentum) / 2)),
      status: score >= 86 ? "Leader" : score >= 78 ? "Scaled" : "Watch",
      sparkline: makeSparkline(score, momentum, index + 1),
      dimensionScores: makeDimensions(score, momentum, quality),
      evidenceCount: sourceIdsForEntity(entityId, quality).length,
      evidenceQuality: quality,
      sourceIds: sourceIdsForEntity(entityId, quality),
      dataStatus: "prototype",
    };
  });

